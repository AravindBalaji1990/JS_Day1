//switch
// the switch will hcek for which condition and directly points to that condition
var grade='B';  
var result;  
switch(grade){  
case 'A':  
result="A Grade";  
console.log(result);  
break;  
case 'B':  
result="B Grade";
console.log(result);  
break;  
case 'C':  
result="C Grade";  
console.log(result);  
break;  
default:  
result="No Grade";  
console.log(result);  
}  

